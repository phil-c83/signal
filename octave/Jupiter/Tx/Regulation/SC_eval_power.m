function fft_sig=show_samples(sig)
  global Fs;
  df = Fs/length(sig);
  fft_sig = fft(sig)/length(sig);
  figure;
  plot((0:length(sig)-1)/Fs,sig);
  figure;
  plot(((2:length(sig))-1)*df,abs(fft_sig(2:length(sig))));
endfunction

function process_samples(sig,fft_sig,Fs,f1,f2)
  global VREFCAN;
  global VCANMAX;
  global Fs;
  df = Fs/length(sig);
  [covxx,covyy,covxy]=covar(sig,sig);
  #fft_power = 2*sum(abs(fft_sig(2:length(sig)/2)).^2)
  # just
  fft_power = 2*sum(abs(fft_sig(round(f1/df+1))).^2 +
                    abs(fft_sig(round(2*f1/df+1))).^2 +
                    abs(fft_sig(round(f2/df+1))).^2   +
                    abs(fft_sig(round(2*f2/df+1))).^2);
  sig_rms = sqrt(eval_power(f1,f2,2/5,covxx,200e-9,31.6*7.02));
  fft_rms = sqrt(eval_power(f1,f2,2/5,fft_power,200e-9,31.6*7.02));

  printf("fft_power=%.2f sig_power=%.2f norm_fft_rms=%.3f norm_sig_rms=%.3f\n",
          fft_power,
          covxx,
          fft_rms*VREFCAN/VCANMAX,
          sig_rms*VREFCAN/VCANMAX);
endfunction

